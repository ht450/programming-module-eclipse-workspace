package assignment.ops;

/**
 * @author Hugh
 *
 */
public class AssignmentOperatorExamples {

	/**
	 * @param args
	 */
	public static void main(String[] args) {
		
		int num;
		num=3;
		
		//long form
		num=num+3;
		System.out.println(num);
		
		//additional compound assignment operator +=
		num+=3;
		System.out.println(num);
		
		// others -= *= /=
		num*=9;
		System.out.println(num);
		
	}

}
