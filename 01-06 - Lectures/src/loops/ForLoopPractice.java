public class ForLoopPractice {

	/**
	 * @param args
	 */
	public static void main(String[] args) {

		System.out.println("Start");
		
		for ( int loop = 10 ; loop >= 5 ; loop-- ) {
			System.out.println(loop);
		}//end of for
		
		System.out.println("End");

	}//end of main method

}
