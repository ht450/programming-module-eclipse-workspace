package loops;

/**
 * @author Hugh
 *
 */
public class ForLooper {

	/**
	 * @param args
	 */
	public static void main(String[] args) {
		
		for (int loop = 100; loop >= 1; loop--) {
			//loop body
		System.out.println(loop);	
		}//end of for
		System.out.println("BLAST OFF!");
	
	}//end of main

}
