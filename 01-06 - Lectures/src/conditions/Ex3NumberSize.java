public class Ex3NumberSize {

	/**
	 * @param args
	 */
	public static void main(String[] args) {
		// input 2 numbers
		int num1, num2;
		
		num1 = 2;
		num2 = 3;
		
		// output bigger number
		if (num1 > num2) {
			System.out.println("Num1 is larger: "+num1);
		} else if (num2 > num1) {
			System.out.println("Num2 is larger: "+num2);
		} else if (num1 == num2) {
			// if they are the same output "Both numbers are the same"
			System.out.println("Both numbers are the same!");
		}
		
		
		
		
		
		
		
	}

}
